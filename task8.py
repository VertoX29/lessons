# 1. Написати функцію, що в діапазоні чисел від m до n
# знаходить найменше ціле число, що ділиться без остачі на k
# m, n, k — параметри функції.

# 2. Ще одна функція з такими ж параметрами,
# але знаходить не найменше, а найбільше число.

def min_num(m, n, k):
    for i in range(m, n+1):
        if i % k == 0:
            return(i)
print(min_num(10, 30, 7))



def max_num(m, n, k):
    for i in range(n, m-1, - 1):
        if i % k == 0:
            return(i)
print(max_num(10, 30, 7))
